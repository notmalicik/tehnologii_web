            Chișinău(în rusă Кишинёв)</i> este capitala, cel mai mare oraș și centrul administrativ, teritorial, economic, științific și cultural al Republicii Moldova.</br>
            </br><b>Orașul</b> este așezat la o margine a pantei de sud-est a Podișului Central al Moldovei, în zona de silvostepă, fiind străbătut de râul Bâc, un afluent de dreapta al Nistrului. La 1 ianuarie 1984, aici locuiau circa 604.500 persoane, conform
            recensământului din 1989 – 661.400, în 1996 – 662.000 persoane. În prezent (2020), Chișinăul găzduiește peste 738.500 de locuitori (estimare). Chișinăul este legat prin căi ferate și drumuri cu toate municipiile, orașele și centrele raionale
            și multe sate din republică, de asemenea cu centre urbane din România, Ucraina, Bulgaria, Turcia, Belarus, Rusia și alte state.</br>
            </br> <b>Din punct de vedere administrativ</b>, este divizat în cinci sectoare: Centru, Botanica, Buiucani, Râșcani și Ciocana. Organul local al puterii de stat este Primăria municipiului (Consiliul municipal). Chișinăul este supranumit „Orașul
            din piatră albă”. Respectivul supranume provine din abundența clădirilor deschise la culoare, fiind construite din piatra albă de calcar. Printre altele, primul vers din forma actuală a imnului orașului Chișinău, intitulat Orașul meu (muzică:
